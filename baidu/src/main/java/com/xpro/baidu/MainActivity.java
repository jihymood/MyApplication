        markerOptions = new MarkerOptions().flat(true).anchor(0.5f, 0.5f).icon(BitmapDescriptorFactory.fromResource(R.mipmap.arrow)).position(polylines.get(0)).rotate((float) getAngle(0));

        // mMoveMarker局部变量，一辆小车一个移动箭头
        Marker mMoveMarker = (Marker) mBaiduMap.addOverlay(markerOptions);

